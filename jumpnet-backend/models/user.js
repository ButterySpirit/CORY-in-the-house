module.exports = (sequelize, DataTypes) => {
  const User = sequelize.define('User', {
    username: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
    jumpHeight: {
      type: DataTypes.FLOAT,
      defaultValue: 1.0,
    },
  });

  return User;
};
